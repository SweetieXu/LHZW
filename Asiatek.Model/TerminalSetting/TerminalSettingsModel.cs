using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Asiatek.Model.TerminalSetting
{
    public class TerminalSettingsModel
    {
        /// <summary>
        /// 终端心跳间隔 s
        /// </summary>
        public uint? HeartbeatInterval { get; set; }

        /// <summary>
        /// 位置汇报策略
        /// </summary>
        public int? LocationReportPolicy { get; set; }

        /// <summary>
        /// 位置汇报方案
        /// </summary>
        public int? LocationReportTrigger { get; set; }

        /// <summary>
        /// 未登录汇报间隔 s
        /// </summary>
        public uint? StateRepotrInterval_NotLoggedIn { get; set; }

        /// <summary>
        /// 休眠汇报间隔 s
        /// </summary>
        public uint? StateRepotrInterval_Sleep { get; set; }

        /// <summary>
        /// 紧急报警汇报间隔 s
        /// </summary>
        public uint? StateRepotrInterval_Urgent { get; set; }

        /// <summary>
        /// 缺省汇报间隔 s
        /// </summary>
        public uint? StateRepotrInterval_General { get; set; }

        /// <summary>
        /// 最高速度 km/h
        /// </summary>
        public uint? MaximumSpeed { get; set; }

        /// <summary>
        /// 超速持续时间 s
        /// </summary>
        public uint? OverspeedCheckTime { get; set; }

        /// <summary>
        /// 超速报警预警差值 km/h
        /// </summary>
        public double? DifferenceOfOverspeedValues { get; set; }

        /// <summary>
        /// 连续驾驶时间门限 s
        /// </summary>
        public uint? MaximumDrivingTime { get; set; }

        /// <summary>
        /// 当天累计驾驶时间门限 s
        /// </summary>
        public uint? MaximumDrivingTimeToday { get; set; }

        /// <summary>
        /// 疲劳驾驶预警差值 s
        /// </summary>
        public ushort? DifferenceOfFatigueDrivingTime { get; set; }

        /// <summary>
        /// 最小休息时间 s
        /// </summary>
        public uint? MinimumBreakTime { get; set; }

        /// <summary>
        /// 最长停车时间 s
        /// </summary>
        public uint? MaximumStoppingTime { get; set; }

        /// <summary>
        /// 车辆里程 km
        /// </summary>
        public double? TotalODO { get; set; }

        /// <summary>
        /// 监听电话号码
        /// </summary>
        public string TelphoneNO { get; set; }
    }
}
